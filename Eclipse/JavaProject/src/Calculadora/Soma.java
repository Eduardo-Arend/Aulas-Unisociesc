package Calculadora;

public class Soma {

	public static int Soma (int a,int b) {
		return a+b;
	}
	public static int soma (float a, float b){
	float r= a+b;
	return (int)r;
	}
	public static int subtracao(int a,int b){
		return a - b;
	}
	public static int multiplicacao(int a,int b){
		return a * b;
	}
	public static int divisao(int a,int b){
		return a / b;
	}

}

